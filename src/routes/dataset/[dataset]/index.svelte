  <h1>
    
      Daten der Verkehrszählung zum motorisierten Individualverkehr (Stundenwerte), seit 2012
      
      
    
  </h1>
  
    
      <div class="notes embedded-content">
        <p>Die hier verfügbaren Datensätze beinhalten die <strong>täglich aktualisierten</strong> Messwerte zum motorisierten Individualverkehr (MIV) in der Stadt Zürich, welche von den zahlreichen Zählstellen der Dienstabteilung Verkehr (DAV) gemessen wurden.</p>
<p>Die Aktualisierung beinhaltet jeweils die Daten von <strong>vorgestern</strong> (z.B. am 17.1.2021 werden die Daten bis und mit dem vollendeten 15.1.2021 geliefert). Bis spätestens 08:00 Uhr sind die neuesten Daten verfügbar.</p>
<p>Zu einer Messstelle gehören mehrere Zählstellen, welche wiederum Messungen mit sogenannten Detektoren vornehmen. In den vorliegenden Datensätzen sind die Messwerte pro Zählstelle enthalten. Für jede Zählstelle gibt es einen detaillierten PDF-Situationsplan in der Datei "Zaehlstellen_Detail.zip", welcher u.a. die genaue Lage der Detektoren anzeigt.</p>
      </div>
    
  
  
